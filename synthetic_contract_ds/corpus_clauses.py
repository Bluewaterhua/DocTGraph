def sample_slots(rng: random.Random, money_raw: str = "100000元") -> Dict[str, str]:
    # 可控的填槽：全部是“类型级”信息，不涉及真实主体
    svc = rng.choice(["系统集成", "软件开发", "数据治理", "安全加固", "运维支持", "测试验证", "方案咨询"])
    deliveries = rng.choice(["实施方案、配置清单、测试报告", "交付清单、验收资料、操作手册", "接口文档、部署脚本、运维手册"])
    pay_mode = rng.choice(["转账", "对公汇款", "分期支付"])
    invoice_type = rng.choice(["增值税普通发票", "增值税专用发票"])
    ip_mode = rng.choice(["归甲方所有", "归乙方所有", "双方共享（按协议）"])
    owner = rng.choice(["甲方", "乙方"])
    cap_mode = rng.choice(["合同总金额为上限", "实际已支付金额为上限", "双方另行约定"])
    court_mode = rng.choice(["合同签订地人民法院", "被告所在地人民法院", "乙方所在地人民法院"])
    arb = rng.choice(["某仲裁委员会", "某国际仲裁中心"])
    return {
        "svc": svc,
        "deliveries": deliveries,
        "pay_mode": pay_mode,
        "invoice_type": invoice_type,
        "ip_mode": ip_mode,
        "owner": owner,
        "cap_mode": cap_mode,
        "court_mode": court_mode,
        "arb": arb,
        "money": money_raw,
        "staff": str(rng.randint(1, 6)),
        "days": str(rng.randint(7, 90)),
        "wkd": str(rng.randint(3, 15)),
        "hours": str(rng.randint(4, 48)),
        "n": str(rng.randint(1, 5)),
        "years": str(rng.randint(1, 5)),
        "months": str(rng.randint(1, 24)),
        "rate": str(rng.choice([0.01, 0.03, 0.05, 0.1]))
    }

def sample_clause_paragraphs(rng: random.Random, k: int, money_raw: str) -> List[str]:
    slots = sample_slots(rng, money_raw=money_raw)
    # 从不同类目混采，避免同一类密集重复
    cats = list(CLAUSE_TEMPLATES.keys())
    rng.shuffle(cats)

    out = []
    while len(out) < k:
        for c in cats:
            if len(out) >= k:
                break
            tpl = rng.choice(CLAUSE_TEMPLATES[c])
            out.append(tpl.format(**slots))
    return out

def sample_noise_paragraphs(rng: random.Random, k: int) -> List[str]:
    return [rng.choice(NOISE_TEMPLATES) for _ in range(k)]
