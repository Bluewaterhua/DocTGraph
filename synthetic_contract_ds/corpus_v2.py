def sample_clause_paragraphs(rng: random.Random, k: int, money_raw: str) -> List[str]:
    """
    Interface required by generate_docs_v2.py:
      sample_clause_paragraphs(rng, k, money_raw=...)
    """
    slots = sample_slots(rng, money_raw=money_raw)

    cats = list(CLAUSE_TEMPLATES.keys())
    rng.shuffle(cats)

    out: List[str] = []
    # 每轮洗牌一次类别顺序，减少“固定顺序指纹”
    while len(out) < k:
        rng.shuffle(cats)
        for c in cats:
            if len(out) >= k:
                break
            tpl = rng.choice(CLAUSE_TEMPLATES[c])
            out.append(tpl.format(**slots))
    return out

def sample_boilerplate(rng: random.Random, k: int = 6) -> List[str]:
    """
    Interface required by generate_docs_v2.py:
      sample_boilerplate(rng, k=...)
    """
    return [rng.choice(NOISE_TEMPLATES) for _ in range(k)]
